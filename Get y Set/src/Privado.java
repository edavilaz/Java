
public class Privado {

		private int edad;
		private String nombre;
		
	// get = obtener
			public int getedad() {
				return edad;
			}
	
	// set = asignar
			public void setedad(int EDAD) {
				edad=EDAD;
			}
			
	// get de nombre
			public String getnombre() {
				return nombre;
			}
	// set de nombre
			public void setnombre(String NOMBRE) {
				nombre = NOMBRE;
			}
			
}

